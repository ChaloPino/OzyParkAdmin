namespace OzyParkAdmin.Domain.Reportes.Charts;

/// <summary>
/// El estilo del punto de un chart tipo linea.
/// </summary>
public enum PointStyle
{
    /// <summary>
    /// Círculo.
    /// </summary>
    Circle = 0,

    /// <summary>
    /// Cruz.
    /// </summary>
    Cross = 1,

    /// <summary>
    /// Cruz rotada.
    /// </summary>
    CrossRot = 2,

    /// <summary>
    /// Guión.
    /// </summary>
    Dash = 3,

    /// <summary>
    /// Línea.
    /// </summary>
    Line = 4,

    /// <summary>
    /// Rectángulo.
    /// </summary>
    Rect = 5,

    /// <summary>
    /// Rectángulo redondeado.
    /// </summary>
    RectRounded = 6,

    /// <summary>
    /// Rectángulo rotado.
    /// </summary>
    RectRot = 7,

    /// <summary>
    /// Estrella.
    /// </summary>
    Star = 8,

    /// <summary>
    /// Triángulo.
    /// </summary>
    Triangle = 9,
}
